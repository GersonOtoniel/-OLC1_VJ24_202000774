public class ReturnStructTypes {

    public String id;
    public Object value;
    public ReturnStructTypes(String id, Object value){
        this.id = id;
        this.value = value;
    }

    

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Object getValue() {
        return value;
    }

    public void setValue(Object value) {
        this.value = value;
    }

   
}
